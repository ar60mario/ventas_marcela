    private void generar(Rubro rubro) {
        String fe;
        String fe1;
        String fe2;
        String fe3;
        Date da = new Date();
        Date da1 = new Date();
        Date da2 = new Date();
        Date da3 = new Date();
        int ps = comboP.getSelectedIndex();
        if(ps < 1){
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR PRODUCTO O SERVICIO");
            comboP.requestFocus();
            return;
        }
        if (!fechaTxt.getText().isEmpty()) {
            fe = fechaTxt.getText();
        } else {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA FECHA");
            fechaTxt.requestFocus();
            return;
        }
        if (!fecha1Txt.getText().isEmpty()) {
            fe1 = fecha1Txt.getText();
        } else {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA FECHA");
            fecha1Txt.requestFocus();
            return;
        }
        if (!fecha2Txt.getText().isEmpty()) {
            fe2 = fecha2Txt.getText();
        } else {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA FECHA");
            fecha2Txt.requestFocus();
            return;
        }
        if (!fecha3Txt.getText().isEmpty()) {
            fe3 = fecha3Txt.getText();
        } else {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA FECHA");
            fecha3Txt.requestFocus();
            return;
        }
        try {
            da = sdf.parse(fe);
            da1 = sdf.parse(fe1);
            da2 = sdf.parse(fe2);
            da3 = sdf.parse(fe3);
        } catch (ParseException ex) {
            Logger.getLogger(GenerarAbonoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        abonos = null;
        try {
            abonos = new AbonoService().getAbonosActivosByRubroAndClienteHabilitado(rubro); //AllClientesHabilitadosByRubro(rubro);
        } catch (Exception ex) {
            Logger.getLogger(GenerarAbonoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (abonos != null && !abonos.isEmpty()) {
            for (Abono ab : abonos) {
                Cliente cl = ab.getCliente();
                ab.setGenerado(true);
//                TitularCuit titu = null;
//                titu = new TitularCuitService().getAllTitularDeCuitActivos();
                List<RenglonAbono> ra = null;
//                cl.setGenerado(true);
                try {
                    ra = new RenglonAbonoService().getRenglonAbonosByAbono(ab);
                } catch (Exception ex) {
                    Logger.getLogger(GenerarAbonoFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
//                System.out.println(cl.getDireccion().getCalle());
//                System.out.println(cl.getDireccion().getNumero());
//                System.out.println(ab.getRubro().getDetalle());
//                for (RenglonAbono ra1 : ra) {
//                    System.out.println(ra1.getOrden());
//                    System.out.println(ra1.getTexto());
//                }
//                System.out.println(ab.getImporte());
//                System.out.println("");
//                JOptionPane.showMessageDialog(this, "VER");
                String resultado = UtilFactura.saveFactura(cl, ab, ra, da, da1, da2, da3, ps);
                if (resultado.equals("N")) {
                    JOptionPane.showMessageDialog(this, "ERROR EN TRANSACCION");
                    return;
                }
            }
            volver();
        } else {
            JOptionPane.showMessageDialog(this, "VERIFIQUE NO HAY ABONOS PARA GENERAR");
        }
    }

