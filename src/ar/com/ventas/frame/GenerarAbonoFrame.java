    private void llenarCombo() {
        combo.removeAllItems();
        combo.addItem("");
        rubros = null;
        try {
            rubros = new RubroService().getAllRubrosActivos();
        } catch (Exception ex) {
            Logger.getLogger(GenerarAbonoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (rubros != null && !rubros.isEmpty()) {
            for (Rubro ru : rubros) {
                combo.addItem(ru.getDetalle());
            }
        }
        comboP.removeAllItems();
        comboP.addItem("");
        comboP.addItem("1. PRODUCTO");
        comboP.addItem("2. SERVICIO");
        comboP.addItem("3. PRODUCTO/SERVICIO");
        comboP.setSelectedIndex(3);
    }
