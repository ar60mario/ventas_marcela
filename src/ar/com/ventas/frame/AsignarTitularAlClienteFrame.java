    private void asignarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asignarBtnActionPerformed
        int row = combo.getSelectedIndex();
        if(row < 1){
            JOptionPane.showMessageDialog(this, "SELECCIONE UN NUEVO TITULAR");
            return;
        }
        TitularCuit ti = titulares.get(row -1);
        cliente.setTitular(ti);
        try {
            new ClienteService().updateCliente(cliente);
            JOptionPane.showMessageDialog(this, "GUARDADO");
            volver();
        } catch (Exception ex) {
            Logger.getLogger(AsignarTitularAlClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR GUARDANDO");
        }
    }//GEN-LAST:event_asignarBtnActionPerformed

