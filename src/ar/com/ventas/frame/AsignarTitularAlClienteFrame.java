    private void limpiarCampos() {
        titularTxt.setText("");
        completarDatos();
    }

    private void completarDatos() {
        razonSocialClienteTxt.setText(cliente.getRazonSocial());
        Direccion dm = cliente.getDireccion();
        String str = dm.getCalle()
                + " "
                + dm.getNumero();
        calleNumeroTxt.setText(str);
        titularTxt.setText(cliente.getTitular().getRazonSocial());
        llenarCombo();
    }

    private void llenarCombo() {
        titulares = null;
        combo.removeAllItems();
        combo.addItem("");
        try {
            titulares = new TitularCuitService().getAllTitularDeCuitActivos();
        } catch (Exception ex) {
            Logger.getLogger(AsignarTitularAlClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(titulares !=null && ! titulares.isEmpty()){
            for(TitularCuit tc:titulares){
                combo.addItem(tc.getRazonSocial());
            }
        }
    }

    private void volver() {
        AbmClienteFrame acf = new AbmClienteFrame();
        acf.setVisible(true);
        this.dispose();
    }
