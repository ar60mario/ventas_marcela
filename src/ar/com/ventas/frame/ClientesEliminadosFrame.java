    private void asignarTitular() {
        int row = tablaClientes.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN CLIENTE");
            return;
        }
        Cliente cl = clientes.get(row -1);
        AsignarTitularAlClienteFrame atacf = new AsignarTitularAlClienteFrame(cl);
        atacf.setVisible(true);
        this.dispose();
    }

    private void activar(Cliente c) {
        c.setActivo(true);
        try {
            new ClienteService().updateCliente(c);
        } catch (Exception ex) {
            Logger.getLogger(ClientesEliminadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
