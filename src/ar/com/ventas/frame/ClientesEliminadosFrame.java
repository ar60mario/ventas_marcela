    private void llenarTabla() {
        UtilFrame.limpiarTabla(tablaClientes);
        clientes = null;
        try {
            clientes = new ClienteService().getAllClientesInactivos();
        } catch (Exception ex) {
            Logger.getLogger(ClientesEliminadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (clientes != null && !clientes.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tablaClientes.getModel();
            for (Cliente cli : clientes) {
                Object ob[] = new Object[6];
                ob[0] = cli.getCodigo();
                ob[1] = cli.getRazonSocial();
                ob[2] = cli.getDireccion().getCalle() + " " + cli.getDireccion().getNumero();
                ob[3] = cli.getCuit();
                ob[4] = cli.getCodigoMauro();
//                if (cli.getGenerado()) {
//                    ob[5] = "Generado";
//                } else {
//                    ob[5] = "Habilitado";
//                }
                tbl.addRow(ob);
            }
            tablaClientes.setModel(tbl);
        }
    }

    private void nuevo() {
        NuevoClienteFrame ncf = new NuevoClienteFrame();
        ncf.setVisible(true);
        this.dispose();
    }

    private void modificar() {
        int row = tablaClientes.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Selecciones un cliente");
            return;
        }
        ModificarClienteFrame mcf = new ModificarClienteFrame(clientes.get(row));
        mcf.setVisible(true);
        this.dispose();
    }

